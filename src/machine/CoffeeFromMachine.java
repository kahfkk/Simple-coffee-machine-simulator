package machine;

public class CoffeeFromMachine {
    int amountOfWater;
    int amountOfMilk;
    int coffeeBeans;
    int countOfCups;
    int money;

    public CoffeeFromMachine(int amountOfWater, int amountOfMilk, int coffeeBeans, int countOfCups, int money) {
        this.amountOfWater = amountOfWater;
        this.amountOfMilk = amountOfMilk;
        this.coffeeBeans = coffeeBeans;
        this.countOfCups = countOfCups;
        this.money = money;
    }

}
